## FAQ

**Will my accent hurt my chances?**
Accent alone has not been shown to reduce interview success rates in most professional contexts. Clarity, confidence, and content matter far more. Interviewers make decisions based on whether they believe you can do the job — not based on accent. What does hurt is when anxiety about your accent causes you to rush or trail off. The fix is practice, not accent elimination.

**Should I mention that English is my second language?**
Only if it's directly relevant (e.g., if you're applying for a role that requires a specific language, or if your international background is a genuine selling point). Otherwise, don't bring it up — it draws attention to something the interviewer may not have flagged as relevant.

**What if I blank out on a word mid-answer?**
Pause, breathe, and rephrase with a simpler word. Don't say "sorry, I don't know the word." Instead, try: *"Let me put it a different way..."* and use simpler language. Professionals do this all the time — it reads as thoughtful, not limited.

**How early should I start preparing?**
Start the day you get the interview confirmation. If you have 7 days, you have enough time to do this properly. If you have 3 days, focus exclusively on: your opening answer, your 3 strongest stories, and 10 minutes of AI practice daily.

**Can AI coaching actually replace a human mock interviewer?**
For many kinds of practice — yes. AI coaching lets you iterate faster, practice at any hour, and focus on specific feedback without the social pressure of human mock interviews. Most candidates who use both (AI practice + one human mock interview) report the best outcomes.

